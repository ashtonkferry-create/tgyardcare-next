-- 4. Workflow status view for dashboard
CREATE OR REPLACE VIEW workflow_status_overview AS
SELECT
  category,
  COUNT(*) as total_workflows,
  COUNT(*) FILTER (WHERE status = 'active') as active,
  COUNT(*) FILTER (WHERE status = 'inactive') as inactive,
  COUNT(*) FILTER (WHERE status = 'error') as errored,
  COUNT(*) FILTER (WHERE last_run_status = 'success') as last_run_ok,
  COUNT(*) FILTER (WHERE last_run_status = 'error') as last_run_failed,
  MAX(last_run_at) as most_recent_run
FROM workflow_registry
GROUP BY category
ORDER BY category;

-- 5. Environment variable requirements view
CREATE OR REPLACE VIEW env_var_requirements AS
SELECT DISTINCT unnest(env_vars_required) as env_var,
  array_agg(DISTINCT workflow_number ORDER BY workflow_number) as used_by_workflows,
  COUNT(DISTINCT workflow_number) as workflow_count
FROM workflow_registry
GROUP BY 1
ORDER BY workflow_count DESC;

-- 6. RLS
ALTER TABLE workflow_registry ENABLE ROW LEVEL SECURITY;
ALTER TABLE workflow_run_log ENABLE ROW LEVEL SECURITY;

DO $$ BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE tablename = 'workflow_registry' AND policyname = 'Service role full access') THEN
    CREATE POLICY "Service role full access" ON workflow_registry FOR ALL USING (auth.role() = 'service_role');
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE tablename = 'workflow_run_log' AND policyname = 'Service role full access') THEN
    CREATE POLICY "Service role full access" ON workflow_run_log FOR ALL USING (auth.role() = 'service_role');
  END IF;
END $$;

