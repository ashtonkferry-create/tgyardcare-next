-- 1. Workflow registry table
CREATE TABLE IF NOT EXISTS workflow_registry (
  id SERIAL PRIMARY KEY,
  workflow_number INT UNIQUE NOT NULL,
  name TEXT NOT NULL,
  description TEXT,
  category TEXT NOT NULL,
  n8n_workflow_id TEXT,
  trigger_type TEXT NOT NULL DEFAULT 'cron',
  schedule TEXT,
  status TEXT NOT NULL DEFAULT 'active',
  env_vars_required TEXT[] DEFAULT '{}',
  credentials_required TEXT[] DEFAULT '{}',
  supabase_tables TEXT[] DEFAULT '{}',
  depends_on INT[] DEFAULT '{}',
  last_run_at TIMESTAMPTZ,
  last_run_status TEXT,
  run_count INT DEFAULT 0,
  error_count INT DEFAULT 0,
  created_at TIMESTAMPTZ DEFAULT now()
);

-- 2. Workflow run log
CREATE TABLE IF NOT EXISTS workflow_run_log (
  id BIGSERIAL PRIMARY KEY,
  workflow_number INT NOT NULL REFERENCES workflow_registry(workflow_number),
  started_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  finished_at TIMESTAMPTZ,
  status TEXT NOT NULL DEFAULT 'running',
  items_processed INT DEFAULT 0,
  error_message TEXT,
  execution_id TEXT
);

